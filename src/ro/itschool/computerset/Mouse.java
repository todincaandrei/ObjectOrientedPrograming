package ro.itschool.computerset;

public class Mouse {
    private int dpi;
    private String name;
    private boolean optic;

    public int getDpi() {
        return dpi;
    }

    public void setDpi(int dpi) {
        this.dpi = dpi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isOptic() {
        return optic;
    }

    public void setOptic(boolean optic) {
        this.optic = optic;
    }
}
